stroka = input ('Введите слово/строку состоящую из 4 символов и более ')
print('1)Третий символ этой строки         :',stroka [2])
print('2)Первые 4 символа этой строки      :',stroka [0:4:1])
print('3)Все символы с четными индексами   :',stroka [1::2])
print('4)Все символы с нечетными индексами :',stroka [0::2])
print('5)Все символы в обратном порядке    :',stroka [::-1])